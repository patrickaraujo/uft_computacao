public class Categoria {
    float x;
    float y;
    
    public Categoria(float y){
        this.y = y/100;
        this.x = (100-y)/100;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
